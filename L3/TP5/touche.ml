let touche_vers_int t =
  match t with
    | 'a' | 'â' | 'à' | 'b' | 'c' | 'ç' | '-' -> 2
    | 'd' | 'e' | 'é' | 'è' | 'ê' | 'ë' | 'f' -> 3
    | 'g' | 'h' | 'i' | 'ï' -> 4
    | 'j' | 'k' | 'l' -> 5
    | 'm' | 'n' | 'o' | 'ô'-> 6
    | 'p' | 'q' | 'r' | 's' -> 7
    | 't' | 'u' | 'ù' | 'v' -> 8
    | 'w' | 'x' | 'y' | 'z' -> 9
    | _ -> assert false